package practice;

public class Operators {
   static public void main(String args[]){
       
       //Java Unary Operator Example: ++ and --
       int x = 10;
       System.out.println(x++);
       System.out.println(++x);
       System.out.println(x--);
       System.out.println(--x);
       
       int a = 10;
       int b = 10;
       System.out.println(a++ + ++a); // print 22
       System.out.println(b++ + b++); // print 21
   } 
}

